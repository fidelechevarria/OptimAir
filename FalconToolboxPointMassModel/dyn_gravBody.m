function [gx,gy,gz] = dyn_gravBody(q0,q1,q2,q3,g)
q00 = q0*q0;
q11 = q1*q1;
q22 = q2*q2;
q33 = q3*q3;
q01 = q0*q1;
% q02 = q0*q2;
q03 = q0*q3;
q12 = q1*q2;
% q13 = q1*q3;
q23 = q2*q3;
% B(1,1) = q00 + q11 - q22 - q33;
% B(1,2) = 2*(q12+q03);
% B(1,3) = 2*(q13-q02);
% B(2,1) = 2*(q12-q03);
% B(2,2) = q00 - q11 + q22 - q33;
% B(2,3) = 2*(q23+q01);
% B(3,1) = 2*(q13+q02);
% B(3,2) = 2*(q23-q01);
% B(3,3) = q00 - q11 - q22 + q33;
gx = - 2*(q12+q03)*g;
gy = - 2*(q23+q01)*g;
gz = - (q00 - q11 - q22 + q33)*g;
end